@skills.register("read_notebook", "Read current notebook state")
def read_notebook(
    notebook_path: str = None
) -> Dict[str, Any]:
    """
    Read the current state of the experiment notebook.

    Args:
        notebook_path: Path to notebook (default: experiments/dili_optimization.ipynb)

    Returns:
        Notebook structure and cell summaries
    """
    path = Path(notebook_path) if notebook_path else NOTEBOOK_PATH

    if not path.exists():
        return {
            "status": "not_found",
            "message": f"Notebook not found at {path}",
            "exists": False
        }

    try:
        with open(path, 'r') as f:
            nb = nbformat.read(f, as_version=4)

        cells_summary = []
        for i, cell in enumerate(nb.cells):
            summary = {
                "index": i,
                "type": cell.cell_type,
                "lines": len(cell.source.split('\n')),
                "preview": cell.source[:100] + "..." if len(cell.source) > 100 else cell.source
            }
            if cell.cell_type == "code" and hasattr(cell, 'outputs'):
                summary["has_output"] = len(cell.outputs) > 0
            cells_summary.append(summary)

        return {
            "status": "success",
            "exists": True,
            "n_cells": len(nb.cells),
            "cells": cells_summary,
            "metadata": nb.metadata.get("dili_agent", {})
        }

    except Exception as e:
        return {"status": "error", "message": str(e)}


@skills.register("create_notebook", "Create a new experiment notebook")
def create_notebook(
    notebook_path: str = None,
    title: str = "DILI Optimization Experiment"
) -> Dict[str, Any]:
    """
    Create a new experiment notebook with default structure.

    Args:
        notebook_path: Path for new notebook
        title: Notebook title

    Returns:
        Creation status
    """
    path = Path(notebook_path) if notebook_path else NOTEBOOK_PATH

    # Ensure directory exists
    path.parent.mkdir(parents=True, exist_ok=True)

    # Create notebook structure
    nb = nbformat.v4.new_notebook()
    nb.metadata["dili_agent"] = {
        "created": datetime.now().isoformat(),
        "last_modified": datetime.now().isoformat(),
        "iterations": 0
    }

    # Add header cells
    header_md = f"""# {title}

**Generated by DILI Optimization Agent**

Created: {datetime.now().strftime('%Y-%m-%d %H:%M')}

## Objective
Optimize ML workflow for Drug-Induced Liver Injury (DILI) prediction.

## Approach
- Feature engineering: Fingerprints, descriptors, bioactivity data
- Model optimization: Multiple classifiers with hyperparameter tuning
- Evaluation: Cross-validation with MCC as primary metric
"""

    imports_code = """# Core imports
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from pathlib import Path

# ML imports
from sklearn.model_selection import train_test_split, cross_val_score
from sklearn.metrics import matthews_corrcoef, roc_auc_score, confusion_matrix
from sklearn.preprocessing import StandardScaler

# RDKit for molecular features
try:
    from rdkit import Chem
    from rdkit.Chem import AllChem, Descriptors
    RDKIT_AVAILABLE = True
except ImportError:
    RDKIT_AVAILABLE = False
    print("RDKit not available - install with: pip install rdkit")

# Set random seed for reproducibility
np.random.seed(42)

print("Imports complete")
"""

    nb.cells.append(nbformat.v4.new_markdown_cell(header_md))
    nb.cells.append(nbformat.v4.new_code_cell(imports_code))

    try:
        with open(path, 'w') as f:
            nbformat.write(nb, f)

        return {
            "status": "success",
            "path": str(path),
            "n_cells": len(nb.cells)
        }

    except Exception as e:
        return {"status": "error", "message": str(e)}


@skills.register("add_cell", "Add a cell to the notebook")
def add_cell(
    content: str,
    cell_type: str = "code",
    position: int = -1,
    notebook_path: str = None
) -> Dict[str, Any]:
    """
    Add a new cell to the experiment notebook.

    Args:
        content: Cell content (code or markdown)
        cell_type: 'code' or 'markdown'
        position: Position to insert (-1 = end)
        notebook_path: Path to notebook

    Returns:
        Updated notebook info
    """
    path = Path(notebook_path) if notebook_path else NOTEBOOK_PATH

    if not path.exists():
        # Create notebook if it doesn't exist
        create_result = create_notebook(str(path))
        if create_result["status"] != "success":
            return create_result

    try:
        with open(path, 'r') as f:
            nb = nbformat.read(f, as_version=4)

        # Create new cell
        if cell_type == "code":
            new_cell = nbformat.v4.new_code_cell(content)
        else:
            new_cell = nbformat.v4.new_markdown_cell(content)

        # Insert at position
        if position < 0 or position >= len(nb.cells):
            nb.cells.append(new_cell)
            actual_position = len(nb.cells) - 1
        else:
            nb.cells.insert(position, new_cell)
            actual_position = position

        # Update metadata
        nb.metadata["dili_agent"]["last_modified"] = datetime.now().isoformat()
        nb.metadata["dili_agent"]["iterations"] = nb.metadata["dili_agent"].get("iterations", 0) + 1

        with open(path, 'w') as f:
            nbformat.write(nb, f)

        return {
            "status": "success",
            "cell_position": actual_position,
            "cell_type": cell_type,
            "n_cells": len(nb.cells)
        }

    except Exception as e:
        return {"status": "error", "message": str(e)}


@skills.register("update_cell", "Update an existing cell")
def update_cell(
    cell_index: int,
    new_content: str,
    notebook_path: str = None
) -> Dict[str, Any]:
    """
    Update content of an existing cell.

    Args:
        cell_index: Index of cell to update
        new_content: New cell content
        notebook_path: Path to notebook

    Returns:
        Update status
    """
    path = Path(notebook_path) if notebook_path else NOTEBOOK_PATH

    if not path.exists():
        return {"status": "error", "message": "Notebook not found"}

    try:
        with open(path, 'r') as f:
            nb = nbformat.read(f, as_version=4)

        if cell_index < 0 or cell_index >= len(nb.cells):
            return {"status": "error", "message": f"Invalid cell index: {cell_index}"}

        nb.cells[cell_index].source = new_content
        nb.metadata["dili_agent"]["last_modified"] = datetime.now().isoformat()

        with open(path, 'w') as f:
            nbformat.write(nb, f)

        return {
            "status": "success",
            "cell_index": cell_index,
            "cell_type": nb.cells[cell_index].cell_type
        }

    except Exception as e:
        return {"status": "error", "message": str(e)}


@skills.register("add_experiment_section", "Add a complete experiment section")
def add_experiment_section(
    section_name: str,
    description: str,
    code_blocks: List[str],
    notebook_path: str = None
) -> Dict[str, Any]:
    """
    Add a complete experiment section with header and code.

    Args:
        section_name: Section title
        description: Section description
        code_blocks: List of code cell contents
        notebook_path: Path to notebook

    Returns:
        Section addition status
    """
    path = Path(notebook_path) if notebook_path else NOTEBOOK_PATH

    # Add markdown header
    header_md = f"""## {section_name}

{description}

*Added: {datetime.now().strftime('%Y-%m-%d %H:%M')}*
"""

    results = []

    # Add header
    result = add_cell(header_md, cell_type="markdown", notebook_path=str(path))
    results.append(result)

    # Add code blocks
    for code in code_blocks:
        result = add_cell(code, cell_type="code", notebook_path=str(path))
        results.append(result)

    success_count = sum(1 for r in results if r.get("status") == "success")

    return {
        "status": "success" if success_count == len(results) else "partial",
        "section_name": section_name,
        "cells_added": success_count,
        "total_attempted": len(results)
    }


