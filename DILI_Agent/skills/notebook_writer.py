from rdkit.Chem import AllChem

def smiles_to_morgan(smiles, radius=2, n_bits=2048):
    """Convert SMILES to Morgan fingerprint."""
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        return None
    fp = AllChem.GetMorganFingerprintAsBitVect(mol, radius, nBits=n_bits)
    return np.array(fp)

# Apply to dataset
fingerprints = []
valid_idx = []

for i, smi in enumerate(df[smiles_col]):
    fp = smiles_to_morgan(smi)
    if fp is not None:
        fingerprints.append(fp)
        valid_idx.append(i)

X = np.array(fingerprints)
y = df.iloc[valid_idx][label_col].values

print(f"Generated fingerprints: {{X.shape}}")
print(f"Valid compounds: {{len(valid_idx)}} / {{len(df)}}")
'''
    elif feature_type == "rdkit_2d":
        code = '''# Generate RDKit 2D descriptors
from rdkit.Chem import Descriptors
from rdkit.ML.Descriptors import MoleculeDescriptors

# Get all descriptor names
descriptor_names = [x[0] for x in Descriptors._descList]
calc = MoleculeDescriptors.MolecularDescriptorCalculator(descriptor_names)

descriptors = []
valid_idx = []

for i, smi in enumerate(df[smiles_col]):
    mol = Chem.MolFromSmiles(smi)
    if mol is not None:
        try:
            desc = calc.CalcDescriptors(mol)
            descriptors.append(list(desc))
            valid_idx.append(i)
        except:
            pass

X = np.array(descriptors)
X = np.nan_to_num(X, nan=0.0, posinf=0.0, neginf=0.0)
y = df.iloc[valid_idx][label_col].values

print(f"Generated descriptors: {X.shape}")
print(f"Descriptor names: {len(descriptor_names)}")
